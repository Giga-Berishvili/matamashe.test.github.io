document.addEventListener('DOMContentLoaded', function() {
    const changeAvatarBtns = document.getElementById('change__avatar');
    const changeAvatarSection = document.getElementById('changeAvatar');
    const closeChangeAvatar = document.getElementById('closeChangeAvatar');

    changeAvatarBtns.addEventListener('click', () => {
        changeAvatarSection.classList.add('showSetting');
    });

    closeChangeAvatar.addEventListener('click', () => {
        changeAvatarSection.classList.remove('showSetting');
    });
   

});

